# math.ceil()- return the ceiling of x, the smallest integer greater than or equal to x . if x is not a float, delegates to x.__ceil__(), which should return an integral value.
import math
a = 10.5
print(math.ceil(a))
x = -20
print(math.fabs(x))# return positive number

a = 5
print(math.factorial(a)) # number must be positive number

b = 10.6

print(math.floor(b))

